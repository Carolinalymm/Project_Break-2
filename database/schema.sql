REVOKE ALL ON TABLE public.carts
FROM anon, authenticated;

REVOKE ALL ON TABLE public.cart_items
FROM anon, authenticated;

REVOKE ALL ON TABLE public.orders
FROM anon, authenticated;

REVOKE ALL ON TABLE public.order_items
FROM anon, authenticated;

GRANT ALL ON TABLE public.users
TO service_role;

GRANT ALL ON TABLE public.products
TO service_role;

GRANT ALL ON TABLE public.carts
TO service_role;

GRANT ALL ON TABLE public.cart_items
TO service_role;

GRANT ALL ON TABLE public.orders
TO service_role;

GRANT ALL ON TABLE public.order_items
TO service_role;

GRANT USAGE, SELECT ON ALL SEQUENCES
IN SCHEMA public
TO service_role;


COMMIT;